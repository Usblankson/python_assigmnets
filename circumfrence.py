# Program to calculate the circumference of a circle 2 * PI * R

# defining the value of PI which  is 3.142

PI = 3.142

# Getting input from the keyboard

radius = input('Enter the raduis of the circle you wish to calculate the radius: ')

# Converting the input from the default String input to float

radius = float(radius)

# Calculating the Circumference using the formula  2 * PI * RADUIS

Circumference = 2 * PI * radius

# Displaying the Cicumference to the output screen

print('The Circumference of the Circle is ,', Circumference)

